console.log("While Loop:")

let num = 0;
 while(num < 5){
    console.log(num);
    num++;
 }

 //================= For Loop ================
 //Structure:
//  for(first part  ;  second part  ;  third part){
//     loop statement;
//  }
console.log("For Loop:")

for(let num = 0; num < 5; num++){
    console.log(num);
}
