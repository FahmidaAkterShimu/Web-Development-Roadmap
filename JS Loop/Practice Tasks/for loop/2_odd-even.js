/***

Subtask-1:

Find all the odd numbers from 61 to 100.

 */
console.log("Odd numbers: ")
for (let i = 61; i <= 100; i++) {
    if (i % 2 !== 0) {
        console.log(i);
    }
}


/***

Subtask-2:

Find all the even numbers from 78 to 98.

 */
console.log("Even numbers: ")
for (let n = 78; n <= 98; n++) {
    if (n % 2 === 0) {
        console.log(n);
    }
}
